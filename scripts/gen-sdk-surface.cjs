"use strict";

// scripts/gen-sdk-surface.cjs — the SINGLE SOURCE OF TRUTH for the SDK's public ABI descriptor.
//
// WHY THIS EXISTS (leverage over a plain name-list pin)
//   A downstream program does NOT integrate against symbol NAMES alone. It integrates against the actual
//   call/return contract: it calls `buildSeal(entries)` with a fixed arity and reads back a result whose
//   `verdict` key holds "ACCEPTED"/"REJECTED"; it hard-codes the seal `KIND` tag and branches on the
//   `SCHEMA_VERSION` number. Those are the things whose SILENT drift actually breaks a consumer — a
//   renamed export is loud and obvious, but a `SEAL_SCHEMA_VERSION` bumped 1→2, a `verifySeal` that
//   stops returning `verdict`, or a `buildSeal` that grows a second required argument all keep the same
//   `name : function` shape and would sail past a name-only guard.
//
//   So this descriptor pins the ABI a consumer really depends on:
//     • functions      -> `name : function/<arity>`     (parameter count is part of the contract)
//     • frozen consts  -> `name : <kind>=<canonicalValue>` (the WIRE VALUE, e.g. the KIND tag / schema #)
//     • verifySeal      -> its RESULT KEY SET (the object shape a consumer destructures)
//
//   It is GENERATED, never hand-maintained. `--write` regenerates the block in docs/SDK.md; `--check`
//   fails (exit 1) if docs/SDK.md, the test pin, or the live exports have drifted apart — so a maintainer
//   runs ONE command instead of hand-editing three places and hoping they match.
//
// PURE / OFFLINE — no chain, no key, no network. Introduces NO new product mechanism; only reflects over
// the already-exported surface.

const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const API_PATH = path.join(ROOT, "index.js");
const DOC_PATH = path.join(ROOT, "docs", "SDK.md");

const MARKER = "<!-- SDK-SURFACE:BEGIN (generated by scripts/gen-sdk-surface.cjs — run `npm run sdk:surface -- --write`) -->";
const END_MARKER = "<!-- SDK-SURFACE:END -->";

// ---------------------------------------------------------------------------
// kindOf(v): the stability-relevant base shape.
// ---------------------------------------------------------------------------
function kindOf(v) {
  if (typeof v === "function") return "function";
  if (v === null) return "null";
  if (Array.isArray(v)) return "array";
  if (typeof v === "object") return "namespace";
  return typeof v; // "string" | "number" | "boolean" | ...
}

// canonicalScalar(v): a byte-deterministic rendering of a PINNED wire value (string / number / boolean).
// Strings are JSON-quoted so whitespace/`:` are unambiguous inside a "name : kind=value" line.
function canonicalScalar(v) {
  return JSON.stringify(v);
}

// abiOf(v): the ABI descriptor fragment for a single value — the part after "name : ".
//   function -> "function/<arity>"      (arity = declared parameter count, part of the call contract)
//   string   -> 'string="..."'          (the frozen wire value)
//   number   -> "number=<n>"            (the frozen wire value)
//   boolean  -> "boolean=<b>"
//   other    -> just the kind (namespaces are expanded by the caller)
function abiOf(v) {
  const kd = kindOf(v);
  if (kd === "function") return `function/${v.length}`;
  if (kd === "string" || kd === "number" || kd === "boolean") return `${kd}=${canonicalScalar(v)}`;
  return kd;
}

// ---------------------------------------------------------------------------
// verifySealResultShape(api): the RESULT KEY SET of verifySeal — the object shape a consumer destructures.
// We build a seal and verify it (pure, in-memory) and record the sorted top-level keys of the result.
// A refactor that renames/drops `verdict`, `accepted`, etc. changes this line and trips the guard.
// ---------------------------------------------------------------------------
function verifySealResultShape(api) {
  // Defensive: a MUTATED surface (used by the non-triviality proofs) may have deleted or retyped
  // buildSeal / verifySeal. In that case the result shape is genuinely different (the contract is
  // broken), so emit a distinct sentinel rather than throwing — the guard should DIFFER, not crash.
  if (typeof api.buildSeal !== "function" || typeof api.verifySeal !== "function") {
    return "<unavailable>";
  }
  try {
    const entries = [{ relPath: "surface-probe.txt", bytes: Buffer.from("probe\n") }];
    const seal = api.buildSeal(entries);
    const res = api.verifySeal(seal, entries);
    if (res === null || typeof res !== "object") return "<non-object>";
    return Object.keys(res).sort().join(",");
  } catch (e) {
    return "<threw>";
  }
}

// ---------------------------------------------------------------------------
// serializeSurface(api): the canonical, byte-deterministic ABI descriptor of the whole public surface.
//   One "path : abi" line per symbol, sorted; a namespace is emitted as its own line plus one
//   "ns.member : abi" line per member (one level deep). A trailing "#verifySeal.result : <keys>" line
//   pins the shape a consumer reads back from verifySeal.
// This is the SINGLE serialization the doc block, the test pin, and this generator all compare on.
// ---------------------------------------------------------------------------
// The ONE symbol whose VALUE is intentionally NOT pinned: apiVersion moves on every release, so it is
// rendered by KIND only. Pinning its value would spuriously fail the contract on a routine version bump.
const VALUE_EXEMPT = new Set(["apiVersion"]);

function serializeSurface(api) {
  const out = [];
  for (const k of Object.keys(api).sort()) {
    const v = api[k];
    if (kindOf(v) === "namespace") {
      out.push(`${k} : namespace`);
      for (const m of Object.keys(v).sort()) {
        out.push(`${k}.${m} : ${abiOf(v[m])}`);
      }
    } else if (VALUE_EXEMPT.has(k)) {
      out.push(`${k} : ${kindOf(v)}`); // kind only, value un-pinned
    } else {
      out.push(`${k} : ${abiOf(v)}`);
    }
  }
  // The behavioral tail: the result shape a consumer destructures from verifySeal.
  out.push(`#verifySeal.result : ${verifySealResultShape(api)}`);
  return out.join("\n");
}

// ---------------------------------------------------------------------------
// Doc block extraction / injection.
// ---------------------------------------------------------------------------
function extractDocBlock(docText) {
  const begin = docText.indexOf(MARKER);
  const end = docText.indexOf(END_MARKER);
  if (begin < 0 || end < 0 || end < begin) return null;
  const between = docText.slice(begin + MARKER.length, end);
  const fence = between.match(/```text\n([\s\S]*?)\n```/);
  return fence ? fence[1] : null;
}

function renderDocBlock(surface) {
  return `${MARKER}\n\`\`\`text\n${surface}\n\`\`\`\n${END_MARKER}`;
}

function writeDocBlock(docText, surface) {
  const begin = docText.indexOf(MARKER);
  const end = docText.indexOf(END_MARKER);
  if (begin < 0 || end < 0 || end < begin) {
    throw new Error("SDK.md is missing the SDK-SURFACE markers; cannot inject the generated block.");
  }
  return docText.slice(0, begin) + renderDocBlock(surface) + docText.slice(end + END_MARKER.length);
}

// ---------------------------------------------------------------------------
// Programmatic API (used by the test — no child process needed).
// ---------------------------------------------------------------------------
function computeSurface() {
  // Fresh require so --write picks up edits without a stale cache in a long-lived process.
  delete require.cache[require.resolve(API_PATH)];
  const api = require(API_PATH);
  return serializeSurface(api);
}

// checkDrift(): returns { ok, surface, docBlock, reasons[] } WITHOUT touching disk.
function checkDrift() {
  const surface = computeSurface();
  const reasons = [];
  let docBlock = null;
  try {
    const docText = fs.readFileSync(DOC_PATH, "utf8");
    docBlock = extractDocBlock(docText);
    if (docBlock == null) reasons.push("docs/SDK.md has no generated ```text surface block between the markers");
    else if (docBlock !== surface) reasons.push("docs/SDK.md surface block has DRIFTED from the live index.js exports");
  } catch (e) {
    reasons.push(`cannot read docs/SDK.md: ${e.message}`);
  }
  return { ok: reasons.length === 0, surface, docBlock, reasons };
}

module.exports = {
  MARKER,
  END_MARKER,
  kindOf,
  abiOf,
  serializeSurface,
  computeSurface,
  extractDocBlock,
  renderDocBlock,
  checkDrift,
  DOC_PATH,
  API_PATH,
};

// ---------------------------------------------------------------------------
// CLI.
//   node scripts/gen-sdk-surface.cjs            -> print the canonical surface descriptor to stdout
//   node scripts/gen-sdk-surface.cjs --check    -> exit 1 if docs/SDK.md has drifted from the exports
//   node scripts/gen-sdk-surface.cjs --write    -> regenerate the block in docs/SDK.md in place
// ---------------------------------------------------------------------------
if (require.main === module) {
  const mode = process.argv[2];
  if (mode === "--write") {
    const surface = computeSurface();
    const docText = fs.readFileSync(DOC_PATH, "utf8");
    const next = writeDocBlock(docText, surface);
    if (next !== docText) {
      fs.writeFileSync(DOC_PATH, next);
      console.log("SDK-SURFACE: docs/SDK.md regenerated.");
    } else {
      console.log("SDK-SURFACE: docs/SDK.md already up to date.");
    }
    process.exit(0);
  } else if (mode === "--check") {
    const { ok, reasons } = checkDrift();
    if (ok) {
      console.log("SDK-SURFACE-OK: docs/SDK.md matches the live index.js exports.");
      process.exit(0);
    }
    console.error("SDK-SURFACE-DRIFT:");
    for (const r of reasons) console.error("  - " + r);
    console.error("  fix: run `npm run sdk:surface -- --write` and update the pin in test/sdk.contract.test.js.");
    process.exit(1);
  } else {
    process.stdout.write(computeSurface() + "\n");
    process.exit(0);
  }
}
