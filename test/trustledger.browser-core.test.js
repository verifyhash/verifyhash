const VENDORED_PATH = path.join(TL, "lib", "sha256-vendored.js");
const LOADER_PATH = path.join(TL, "lib", "policy-bundled-loader.js");
const PINNED_PATH = path.join(__dirname, "fixtures", "trustledger-close-digests-pinned.json");

const vendored = require(VENDORED_PATH);
const { sha256, sha256Hex, sha256HexUtf8, utf8Bytes } = vendored;

